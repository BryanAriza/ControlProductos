    private void BotonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonConsultarActionPerformed
        String identificacion = CampoID.getText();
        String user = "elian";
        String pass = "15092000";
        String dbname = "controlproductos";
        String url = "jdbc:mysql://localhost:3306/" + dbname;
        System.out.println("Conectandoâ¦");
        try (Connection connection = DriverManager.getConnection(url, user, pass)) {

            Statement s = connection.createStatement();
            String query2 = "SELECT Nombre, Codigo, Departamento FROM investigador WHERE Identificacion = \"" + identificacion + "\"";
            System.out.println(query2);
            ResultSet r = s.executeQuery(query2);

            if (r.next()) {
                CampoNombre.setText(r.getString("Nombre"));
                CampoNombre.setEditable(true);
                CampoCodigo.setText(r.getString("Codigo"));
                CampoCodigo.setEditable(true);
                CampoDepartamento.setText(r.getString("Departamento"));
                CampoDepartamento.setEditable(true);
                CampoID.setEditable(false);

            } else {
                System.out.println("No hay registros");
            }
            connection.close();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_BotonConsultarActionPerformed

